<h1>PYP Learner Profile Booklist</h1>

<p>This is a list of picture books grouped by IB Learner Profile attribute. Each book is chosen for it's utility in facilitating discussion of a particular learner profile attribute. Recommended books are books which have a connection to the themes, but cannot be found in digital form or in the school library.</p>

<p>If you would like to add to this list please email <a
href="mailto:jamessmith.cwa@wes-group.org">James Smith</a>.</p>

